package academy.devdojo.maratonajava.introducao.exercicios_arrays;

import java.util.Locale;

public class lixo {

    static void main(String[] args) {
        String nome = "Francisco";

        System.out.println(nome);

        System.out.println();

        System.out.println(nome.contains("F"));
        System.out.println();

        System.out.println(nome.toLowerCase(Locale.ROOT));
        System.out.println(nome.toUpperCase());

        System.out.println();

        System.out.println(nome.equals("Francisco"));
    }
}
